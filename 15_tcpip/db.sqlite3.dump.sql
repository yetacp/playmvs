CREATE TABLE IF NOT EXISTS "clients" (
	"id"	INTEGER NOT NULL,
	"firstname"	TEXT NOT NULL,
	"lastname"	TEXT NOT NULL,
	"email"	TEXT,
	"phone"	TEXT,
	"address"	TEXT,
	"zip"	TEXT,
	"city"	TEXT,
	"state"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "employees" (
	"id"	INTEGER NOT NULL,
	"firstname"	TEXT NOT NULL,
	"lastname"	TEXT NOT NULL,
	"birth"	TEXT,
	"email"	TEXT,
	"phone"	TEXT,
	"address"	TEXT,
	"zip"	TEXT,
	"idjob"	INTEGER NOT NULL,
	"city"	TEXT,
	"state"	TEXT,
	CONSTRAINT "job" FOREIGN KEY("idjob") REFERENCES "jobs",
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "jobs" (
	"id"	INTEGER NOT NULL,
	"name"	TEXT NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "orders" (
	"id"	INTEGER NOT NULL,
	"idclient"	INTEGER NOT NULL,
	"idproduct"	INTEGER NOT NULL,
	"quantity"	INTEGER NOT NULL,
	"data"	TEXT NOT NULL,
	FOREIGN KEY("idclient") REFERENCES "clients",
	FOREIGN KEY("idproduct") REFERENCES "products",
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "products" (
	"id"	INTEGER NOT NULL,
	"name"	TEXT NOT NULL,
	"quantity"	INTEGER NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "users" (
	"login"	TEXT NOT NULL,
	"password"	TEXT NOT NULL,
	"idrole"	INTEGER NOT NULL,
	FOREIGN KEY("idrole") REFERENCES "roles",
	PRIMARY KEY("login")
);
CREATE TABLE IF NOT EXISTS "roles" (
	"id"	INTEGER NOT NULL,
	"name"	TEXT NOT NULL,
	PRIMARY KEY("id")
);
